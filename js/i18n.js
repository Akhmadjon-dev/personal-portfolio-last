(function() {
  var currentLang = localStorage.getItem('portfolio_lang') || 'en';

  function applyTranslations(lang) {
    currentLang = lang;
    localStorage.setItem('portfolio_lang', lang);
    document.documentElement.lang = lang;

    var t = translations[lang];
    if (!t) return;

    var elements = document.querySelectorAll('[data-i18n]');
    for (var i = 0; i < elements.length; i++) {
      var el = elements[i];
      var key = el.getAttribute('data-i18n');
      if (t[key]) {
        el.textContent = t[key];
      }
    }

    var placeholders = document.querySelectorAll('[data-i18n-placeholder]');
    for (var i = 0; i < placeholders.length; i++) {
      var el = placeholders[i];
      var key = el.getAttribute('data-i18n-placeholder');
      if (t[key]) {
        el.placeholder = t[key];
      }
    }

    var titles = document.querySelectorAll('[data-i18n-title]');
    for (var i = 0; i < titles.length; i++) {
      var el = titles[i];
      var key = el.getAttribute('data-i18n-title');
      if (t[key]) {
        el.title = t[key];
      }
    }

    // Update active language button
    var btns = document.querySelectorAll('.lang-switcher .lang-btn');
    for (var i = 0; i < btns.length; i++) {
      btns[i].classList.remove('lang-active');
      if (btns[i].getAttribute('data-lang') === lang) {
        btns[i].classList.add('lang-active');
      }
    }
  }

  // Initialize on DOM ready
  document.addEventListener('DOMContentLoaded', function() {
    applyTranslations(currentLang);

    // Bind language buttons
    var btns = document.querySelectorAll('.lang-switcher .lang-btn');
    for (var i = 0; i < btns.length; i++) {
      btns[i].addEventListener('click', function(e) {
        e.preventDefault();
        applyTranslations(this.getAttribute('data-lang'));
      });
    }
  });

  window.applyTranslations = applyTranslations;
})();
